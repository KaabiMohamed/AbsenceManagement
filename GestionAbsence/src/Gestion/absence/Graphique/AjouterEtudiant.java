private void ajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterActionPerformed
Etudiant etudiant;
DAOEtudiant DAOe = new DAOEtudiant();
int cinn = Integer.parseInt(cin.getText());
String name=nom.getText();
String pname= prenom.getText();
String mail = email.getText();
int phone = Integer.parseInt(gsm.getText());
String logg = login.getText();
String pssd = pwd.getText();
String sx = sexe.getSelectedItem().toString();
String cls =(String) classe.getSelectedItem();
int niv = Integer.parseInt(niveau.getSelectedItem().toString());
String brnch = branche.getSelectedItem().toString();
DAOClasse e = new DAOClasse();
Classe c = e.findNomNiveauBra(niv, cls, brnch);
e.toString();
etudiant = new Etudiant(cinn,name,pname,c,sx,mail,phone,logg,pssd);
//DAOe.ajouter(etudiant);

Etudiant var = DAOe.find(cinn);

if (var==null){
     var = new Etudiant();
}
if(var.equals(etudiant)){
    JOptionPane.showMessageDialog(rootPane, "Etudiant Existant dans la base");
}
else {
    DAOe.ajouter(etudiant);
    JOptionPane.showMessageDialog(rootPane, "Etudiant Ajout√© a la base");
}

}//GEN-LAST:event_ajouterActionPerformed

