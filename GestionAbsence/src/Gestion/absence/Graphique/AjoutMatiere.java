private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
Matiere matiere;
DAOMatiere DAOm = new DAOMatiere();
String champ1 = nom.getText();
String champ2 = classe.getSelectedItem().toString();
int champ3 = Integer.parseInt(nbAbs.getText());
int champ4 = Integer.parseInt(nbAbsMax.getText());
int champ5 = Integer.parseInt(nbSeance.getText());

DAOClasse DAOc = new DAOClasse();
Classe c = DAOc.findByName(champ2);
matiere = new Matiere(c,champ1,champ5,champ3,champ4);
Matiere var = DAOm.findNom(champ1);
if (var==null){
    var = new Matiere();
}
if (var.equals(matiere)){
    JOptionPane.showMessageDialog(rootPane, "Matiere Existante dans la base");
}
else{
DAOm.ajouter(matiere);
JOptionPane.showMessageDialog(rootPane, "MAtiere Ajout√©e dans la base");
}
}//GEN-LAST:event_jButton1ActionPerformed

